
class World {
  int totalCases;
  int newCases;
  int totalDeaths;
  int newDeaths;
  int totalRecovered;
  int activeCases;

  World(int totalCases,int newCases,int totalDeaths,int newDeaths,int totalRecovered,int activeCases){ 
    this.totalCases = totalCases;
    this.newCases = newCases;
    this.totalDeaths = totalDeaths;
    this.newDeaths = newDeaths;
    this.totalRecovered = totalRecovered;
    this.activeCases = activeCases;
  }
  
}
